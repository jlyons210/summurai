"""
ANSI color codes
"""

class AnsiColors:
    """
    ANSI color codes
    """

    black       = '\033[30m'
    red         = '\033[31m'
    green       = '\033[32m'
    yellow      = '\033[33m'
    blue        = '\033[34m'
    magenta     = '\033[35m'
    cyan        = '\033[36m'
    white       = '\033[37m'
    reset       = '\033[0m'
    bold        = '\033[1m'
    underline   = '\033[4m'
    invert      = '\033[7m'
    clear       = '\033[2J\033[H'
    clear_line  = '\033[K'

